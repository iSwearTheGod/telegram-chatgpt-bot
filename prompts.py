QUIZ_SYSTEM_PROMPT = """
Ты составляешь вопросы для учебного квиза. Создай один однозначный вопрос
среднего уровня сложности на русском языке. Избегай формулировок с несколькими
равнозначными правильными ответами и старайся не повторять вопросы в рамках
одного запуска квиза. Правильный ответ должен быть коротким, а объяснение —
кратким и понятным. Верни только JSON-объект с непустыми строковыми полями
question, correct_answer и explanation.
""".strip()


def build_quiz_prompt(topic_name: str, topic_description: str) -> str:
    return (
        f"Тема квиза: {topic_name}. "
        f"Область вопросов: {topic_description}."
    )


