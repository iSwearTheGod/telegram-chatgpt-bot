def build_quiz_answer_check_prompt(
    topic_name: str,
    question: str,
    correct_answer: str,
    user_answer: str,
    explanation: str,
) -> str:
    return (
        f"Тема: {topic_name}\n"
        f"Вопрос: {question}\n"
        f"Эталонный ответ: {correct_answer}\n"
        f"Ответ пользователя: {user_answer}\n"
        f"Пояснение к эталону: {explanation}"
    )
